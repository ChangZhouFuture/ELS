package po.stockPO;

public class StockCheckPO {
	private String area;
	private int outBoundNum;
	private int storageNum;
	private double outBoundAmount;
	private double storageAmout;
	
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public int getOutBoundNum() {
		return outBoundNum;
	}
	public void setOutBoundNum(int outBoundNum) {
		this.outBoundNum = outBoundNum;
	}
	public int getStorageNum() {
		return storageNum;
	}
	public void setStorageNum(int storageNum) {
		this.storageNum = storageNum;
	}
	public double getOutBoundAmount() {
		return outBoundAmount;
	}
	public void setOutBoundAmount(double outBoundAmount) {
		this.outBoundAmount = outBoundAmount;
	}
	public double getStorageAmout() {
		return storageAmout;
	}
	public void setStorageAmout(double storageAmout) {
		this.storageAmout = storageAmout;
	}
	
}
